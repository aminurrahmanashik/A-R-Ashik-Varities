
cars = ['bmw','audi','toyota','sabaru']

print(cars)

# sorting the list in reverse order (just order not alphabetically)
cars.reverse()
print(cars)

# simply sort the list alphabetically
cars.sort() 
print(cars)

# sorting the list alphabetically in reverse order 
cars.sort(reverse  = True)
print(cars)

# temporary sorting the alphabetically list not permanently
print(sorted(cars))

print(cars)
print(len(cars))